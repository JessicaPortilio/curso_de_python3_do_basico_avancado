"""
Operadores de atribuição
= += -= *= /= //= **= %=
"""

contador = 10
# soma 
contador += 1 # 11
print(contador)
# substrair 
contador -= 1 #10
print(contador)
# multiplicação 
contador *= 2 #20
print(contador)
# divisão
contador /= 2 #10.0
print(contador)
# parte inteira da divisão
contador //= 2 #5.0
print(contador)
# pontenciação
contador **= 2 #25.0
print(contador)
# resto da divisão
contador %= 2 # 1.0
print(contador)